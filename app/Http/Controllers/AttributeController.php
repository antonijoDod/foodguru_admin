class AttributeController extends Controller
{
    /**
     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index()
    {
        return Inertia::render('attributes',
        [
            "attributes" => Attribute::all()
        ]
    );
    }

